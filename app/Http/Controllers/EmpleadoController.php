    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'nombre' => 'required',
            'apellido' => 'required',
            'direccion' => 'required',
            'email' => 'required',
            'telefono' => 'required',

        ]);
        try{
            $emp = new Empleado();
            $emp->nombre = $request->nombre;
            $emp->apellido = $request->apellido;
            $emp->direccion = $request->direccion;
            $emp->email = $request->email;
            $emp->telefono = $request->telefono;

            $emp->save();
            return redirect()->route("empleados.index")
                ->with('success','Se ha creado el nuevo empleado correctamente');
        }
        catch(\Exception $e){
            return redirect()->route("empleados.index")
                ->with('error','Se ha producido un error guardando el empleado.');
        }
    }

